def test_rules_exempt_dirs_skip_m6():
    """Files under `rules.exempt_dirs` must NOT emit M6 warnings for missing
    recommended frontmatter keys."""
    with tempfile.TemporaryDirectory() as tmp:
        root = make_vault(Path(tmp))
        cfg = load_config(root)
        cfg["rules"] = {"exempt_dirs": ["_archive", "_secretary"]}
        cfg["canonical_folders"]["top_level"] = list(
            cfg["canonical_folders"]["top_level"]
        ) + ["_secretary"]
        # Switch the test fixture from M1-driven required keys to the real
        # recommended-keys mode so M6 fires in the first place.
        cfg["frontmatter"]["required"] = []
        cfg["frontmatter"]["recommended"] = ["title", "created"]

        # Exempt file — no frontmatter — must NOT fire M6.
        write_note(root / "_archive" / "no-fm.md", body="Just a body.\n")
        write_note(root / "_secretary" / "no-fm.md", body="Just a body.\n")
        # Non-exempt file — no frontmatter — MUST fire M6.
        write_note(root / "1-projects" / "alpha" / "no-fm.md", body="Just a body.\n")

        report = vault_check.walk_vault(root, cfg)
        m6_paths = paths_for_rule(report, "M6")
        assert any("alpha/no-fm.md" in p for p in m6_paths), \
            f"expected M6 on non-exempt path; got {m6_paths}"
        assert not any("_archive" in p for p in m6_paths), \
            f"_archive file should be exempt from M6; got {m6_paths}"
        assert not any("_secretary" in p for p in m6_paths), \
            f"_secretary file should be exempt from M6; got {m6_paths}"


def test_append_only_files_skip_m6():
    """Files listed in `append_only.files` must NOT emit M6 warnings."""
    with tempfile.TemporaryDirectory() as tmp:
        root = make_vault(Path(tmp))
        cfg = load_config(root)
        # Remove inbox/todo from protected.files so vault-check actually
        # reads them (the default fixture protects them).
        cfg["protected"]["files"] = []
        cfg["append_only"] = {"files": ["inbox.md", "todo.md"]}
        cfg["frontmatter"]["required"] = []
        cfg["frontmatter"]["recommended"] = ["title", "created"]

        (root / "inbox.md").write_text("# Inbox\n", encoding="utf-8")
        (root / "todo.md").write_text("# Todo\n", encoding="utf-8")
        # Non-exempt root file — MUST fire M6.
        (root / "regular-root-note.md").write_text("# Regular\n", encoding="utf-8")

        report = vault_check.walk_vault(root, cfg)
        m6_paths = paths_for_rule(report, "M6")
        assert any("regular-root-note.md" in p for p in m6_paths), \
            f"expected M6 on regular root note; got {m6_paths}"
        assert not any(p == "inbox.md" for p in m6_paths), \
            f"inbox.md should be exempt from M6; got {m6_paths}"
        assert not any(p == "todo.md" for p in m6_paths), \
            f"todo.md should be exempt from M6; got {m6_paths}"


