GOOD_FRONTMATTER = """---
title: "A good note"
created: 2026-04-11
---

Body.
"""


def make_vault(tmp: Path) -> Path:
    """Build a minimal-but-valid vault at tmp and return its path."""
    root = tmp / "vault"
    root.mkdir()
    # .git marker so find_vault_root() works if called
    (root / ".git").mkdir()
    # _meta layout
    meta = root / "_meta"
    meta.mkdir()
    (meta / "core").mkdir()
    (meta / "core" / "config.jsonc").write_text(MINIMAL_CONFIG_JSONC, encoding="utf-8")
    # Canonical top-level folders, each with a .gitkeep
    for top in ("journal", "1-projects", "2-areas", "3-resources", "4-atlas", "5-cards", "_archive"):
        (root / top).mkdir()
        (root / top / ".gitkeep").write_text("", encoding="utf-8")
    return root


def write_note(path: Path, body: str = GOOD_FRONTMATTER) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(body, encoding="utf-8")


def rules_in(report) -> set[str]:
    return {f.rule for f in report.findings}


def paths_for_rule(report, rule: str) -> list[str]:
    return [f.path for f in report.findings if f.rule == rule]


