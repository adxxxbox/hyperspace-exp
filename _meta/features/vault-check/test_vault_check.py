def test_outline_reachability_o1():
    """O1 — notes not reachable from the master outline are flagged when
    outline.enforce_reachability=True. Reachable notes pass."""
    with tempfile.TemporaryDirectory() as tmp:
        root = make_vault(Path(tmp))
        cfg = load_config(root)
        # Opt in to O1 (test config has no outline section by default).
        cfg["outline"] = {
            "master_moc": "outlines",
            "enforce_reachability": True,
            "exempt_dirs": ["_archive"],
        }
        # Master outline at vault root, links to a hub.
        (root / "outlines.md").write_text(
            "---\ntitle: Outlines\ncreated: 2026-04-20\n---\n\n[[my-hub]]\n",
            encoding="utf-8",
        )
        # Hub links to a leaf.
        write_note(
            root / "4-atlas" / "my-hub.md",
            body="---\ntitle: Hub\ncreated: 2026-04-20\n---\n\n[[reachable-leaf]]\n",
        )
        # Reachable leaf — has no outbound link, but is reached from hub.
        write_note(root / "1-projects" / "alpha" / "reachable-leaf.md")
        # Orphan — no inbound link from outlines chain.
        write_note(root / "1-projects" / "alpha" / "orphan-leaf.md")
        # Archived note — exempt from O1.
        write_note(root / "_archive" / "1-projects" / "old" / "archived-note.md")

        report = vault_check.walk_vault(root, cfg)
        o1_paths = paths_for_rule(report, "O1")
        assert any("orphan-leaf.md" in p for p in o1_paths), \
            f"expected orphan-leaf to fire O1; got {o1_paths}"
        assert not any("reachable-leaf.md" in p for p in o1_paths), \
            f"reachable-leaf should not fire O1; got {o1_paths}"
        assert not any("archived-note.md" in p for p in o1_paths), \
            f"archived note should be exempt; got {o1_paths}"
        assert not any("outlines.md" in p for p in o1_paths), \
            f"outlines.md is the root and should not fire; got {o1_paths}"


