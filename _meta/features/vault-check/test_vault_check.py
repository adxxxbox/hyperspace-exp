def test_rules_exempt_dirs_skip_l3():
    """Slug collisions among paths under `rules.exempt_dirs` must NOT fire L3,
    and a collision between an exempt path and a non-exempt path must NOT fire
    on the non-exempt path (the exempt path doesn't participate)."""
    with tempfile.TemporaryDirectory() as tmp:
        root = make_vault(Path(tmp))
        cfg = load_config(root)
        cfg["rules"] = {"exempt_dirs": ["_archive", "_secretary"]}
        # Allow _secretary as a top-level folder in this fixture.
        cfg["canonical_folders"]["top_level"] = list(
            cfg["canonical_folders"]["top_level"]
        ) + ["_secretary"]

        # Two collisions to exercise:
        # (a) Two files named SKILL.md under _secretary/ — both exempt → no L3.
        write_note(root / "_secretary" / "a" / "SKILL.md")
        write_note(root / "_secretary" / "b" / "SKILL.md")
        # (b) Root CLAUDE.md (non-exempt) + _archive/.../CLAUDE.md (exempt) → no L3.
        write_note(root / "CLAUDE.md")
        write_note(root / "_archive" / "old" / "CLAUDE.md")

        report = vault_check.walk_vault(root, cfg)
        l3_paths = paths_for_rule(report, "L3")
        assert not l3_paths, \
            f"expected no L3 findings; got {l3_paths}"


